SELECT DISTINCT
	T0.U_PrjCode AS PROJECTCODE,
	T0.U_PrjName AS PROJECTNAME,
	T1.U_ItemCode AS ITEMCODE,
	T1.U_ItemName AS ITEMNAME,
	T1.U_D1 AS D1,
	T1.U_D2 AS D2,
	T1.U_D3 AS D3,
	T1.U_D4 AS D4,
	T1.U_D5 AS D5,
	(SELECT SUM(B1.U_RateEx) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS RATEEX,

	(SELECT SUM(B1.U_RateIn) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS RATEIN,

	(SELECT SUM(B1.U_QtyEx) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS QTYEX,

	(SELECT SUM(B1.U_QtyInt) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS QTYIN,

	(SELECT SUM(B1.U_TotalEx) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS TOTALEX,

	(SELECT SUM(B1.U_TotalIn) FROM [@BOQ] B0 INNER JOIN [@BOQ1] B1 ON B1.DocEntry = B0.DocEntry
		AND B0.U_PrjCode = T0.U_PrjCode AND B1.U_ItemCode = T1.U_ItemCode
		AND B1.U_D1 = T1.U_D1 AND B1.U_D2 = T1.U_D2 AND B1.U_D3 = T1.U_D3
		AND B1.U_D4 = T1.U_D4 AND B1.U_D5 = T1.U_D5) AS TOTALEX
FROM
	[@BOQ] T0
	INNER JOIN [@BOQ1] T1 ON T0.DocEntry = T1.DocEntry
WHERE
	T0.U_PrjCode = 'RV-HB02L01'